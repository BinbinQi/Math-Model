        function [fvl, tbl, bys, byf,isw] = compute_best_solution(road, data)
            [buy_w, buy_f, is_wk, road] = common_tool.Help.generate_variable(road, data, 1);
            [C, F, s_w, s_f, m] = common_tool.Help.generate_model(road, data, buy_w, buy_f, is_wk);
            fvl = common_tool.Help.solve_model(C,F);
            tbl = table(road', value(m)',value(s_w)', value(s_f)');
            idx =find(tbl.(1)==0);
            tbl.(1)(idx) = tbl.(1)(idx-1);
            [bys, byf, isw] = deal(value(buy_w),value(buy_f),value(is_wk));
        end
    end
    

