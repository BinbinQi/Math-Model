function [s_xh, w_xh] = getxh(data, road, i,  is_wk)
s_xh = data.sxh_d(i)*2;
w_xh = data.wxh_d(i)*2;
if ismember(road(i), data.ks)
    idx = nnz(ismember(road(1:i), data.ks));
    s_xh = s_xh + data.sxh_d(i) * (2* is_wk(idx)-1);
    w_xh = w_xh + data.wxh_d(i) *(2* is_wk(idx)-1);
elseif data.tq(i) == "沙暴"
    s_xh = s_xh - data.sxh_d(i);
    w_xh = w_xh - data.wxh_d(i);
end
