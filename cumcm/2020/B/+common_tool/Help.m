    %% generate the model
    methods(Static)
        
        function [buy_w, buy_f, is_wk, road] = generate_variable(road, data, flag, rate)
            road(ismember(road(1:end-1),data.ks)&~ismember(road(2:end),data.ks)) = 0;
            ncz = nnz(ismember(road,data.cz));
            nks = nnz(ismember(road,data.ks));
            % 变量
            if flag == 1
                buy_w = intvar(1, ncz + 1);
                buy_f   = intvar(1, ncz + 1);
                is_wk = binvar(1, nks);
            else  % 数据
                buy_w = zeros(1, ncz + 1);
                buy_f   = zeros(1, ncz + 1);
                is_wk = ones(1, nks);
                
                buy_w(1) = floor(data.fzsx * rate/data.szl);
                buy_f(1) = floor((data.fzsx - data.fzsx * rate)/data.wzl);
            end
            
        end
        
        function [C, F, s_w, s_f, m] = generate_model(road, data, buy_w, buy_f, is_wk)
            s = 1;
            t = 1;
            for i = 1 : length(road)
                if i == 1
                    m(i) = data.cszj - buy_w(i)*data.sjg - buy_f(i)*data.wjg;
                    s_w(i) = buy_w(i);
                    s_f(i) = buy_f(i);
                elseif ismember(road(i), data.cz)
                    t = t + 1;
                    m(i) = m(i-1) - buy_w(t)*data.sjg*2 - buy_f(t)*data.wjg*2;
                    [s_xh, w_xh] = getxh(data, road, i-1,  is_wk);
                    s_w(i) = s_w(i-1) - s_xh + buy_w(t);
                    s_f(i) = s_f(i-1) - w_xh + buy_f(t);
                    ss_w(t) = s_w(i-1) - s_xh;
                    ss_f(t) = s_f(i-1) - w_xh;
                elseif ismember(road(i), data.ks) || road(i) == 0
                    if s == 1
                        m(i) = m(i-1);
                    else
                        m(i) = m(i-1) + (ismember(road(i-1),data.ks))*is_wk(s-1)*data.jisy;
                    end
                    [s_xh, w_xh] = getxh(data, road, i-1,  is_wk);
                    s_w(i) = s_w(i-1) - s_xh;
                    s_f(i) = s_f(i-1) - w_xh;
                    if road(i)~=0
                        s = s + 1;
                    end
                else
                    m(i) = m(i-1);
                    [s_xh, w_xh] = getxh(data, road, i-1,  is_wk);
                    s_w(i) = s_w(i-1) - s_xh;
                    s_f(i) = s_f(i-1) - w_xh;
                end
            end
            m(length(road)) = m(length(road)) + s_w(length(road)) * data.sjg*0.5 + s_f(length(road)) * data.wjg*0.5;
            fz = s_w*data.szl + s_f*data.wzl;
            C = [buy_w>=0, buy_f>=0, s_w>=0, s_f>=0,fz<=data.fzsx];
            if any(ismember(road, data.cz))
                C = [C, ss_w>=0, ss_f>=0];
            end
            F = -m(end);
        end
    end
    
    %% solve model
    methods(Static)
        function [fvl, bys, byf, isw] = solve_model(C,F)
            options = sdpsettings('solver','gurobi','verbose', 0);
            diagnostics  = optimize(C,F,options);
            if diagnostics.problem == 0
                fvl = -value(F);
            else
                fvl = -1;
            end         
        end
    end
