        function data = get_data(file_name, n)
            data = load(file_name);
            data.m = data.m + 1;
            
            iddx = double(categorical(data.tq,data.tqh ));
            data.sxh_d = data.sxh(iddx);
            data.wxh_d = data.wxh(iddx);
            admx = common_tool.Help.get_admx(n);
            admx = admx + admx';
            data.G = graph(admx);            
            data.tq(end+1:end+10) = "晴天";
            if n == 1
                data.ks = 12;   % 矿山
                data.cz = 15;   % 村庄
            else
                data.ks = [30 55];   % 矿山
                data.cz = [39 62];   % 村庄
            end
        end
        
